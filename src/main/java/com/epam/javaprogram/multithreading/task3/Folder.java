package com.epam.javaprogram.multithreading.task3;

public class Folder {
    private long numberFile;
    private long numberFolder;
    private long size;

    public long getNumberFile() {
        return numberFile;
    }

    public void setNumberFile(long numberFile) {
        this.numberFile = numberFile;
    }

    public long getNumberFolder() {
        return numberFolder;
    }

    public void setNumberFolder(long numberFolder) {
        this.numberFolder = numberFolder;
    }

    public long getSize() {
        return size;
    }

    public void setSize(long size) {
        this.size = size;
    }
}
